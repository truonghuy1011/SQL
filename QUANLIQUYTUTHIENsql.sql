CREATE DATABASE QUANLIQUYTUTHIEN
GO
USE QUANLIQUYTUTHIEN
GO
--TAO BANG
CREATE TABLE NHANVIEN(
   MANV CHAR(10) NOT NULL ,--KHOA CHINH
   TENNV NVARCHAR(100),
   CMNDNV CHAR(10),
   SDTNV CHAR(10),
   DCNV NVARCHAR(200),
   LUONG MONEY,
   MAPB CHAR(10)NOT NULL,
   MACV CHAR(10) NOT NULL,
   PRIMARY KEY(MANV,MAPB,MACV),
 FOREIGN KEY (MAPB) REFERENCES PHONGBAN(MAPB),
 FOREIGN KEY (MACV) REFERENCES CONGVIEC(MACV),
)
GO

 CREATE TABLE PHONGBAN(
   MAPB CHAR(10)NOT NULL PRIMARY KEY,
   TENPB NVARCHAR(50),
)
GO

CREATE TABLE CONGVIEC(
   MACV CHAR(10)NOT NULL PRIMARY KEY,
   TENCV NVARCHAR(100),
   NHIEMVU NVARCHAR(200),
)
GO

CREATE TABLE THUQUY(
   MATQ CHAR(10)NOT NULL PRIMARY KEY,
   TENTQ NVARCHAR(50),
   CMNDTQ CHAR(10),
   SDTTQ CHAR(10),
   DCTQ NVARCHAR(200),
   NGAYTHU DATETIME,
   NGAYCHI DATETIME,
)
GO
CREATE TABLE NHAHAOTAM(
   MANHT CHAR(10)NOT NULL PRIMARY KEY,
   TENNHT NVARCHAR(100),
   SDTNHT CHAR(10),
   DCNHT NVARCHAR(200),
   DONGGOP NVARCHAR(50),
   NGAYDONGGOP DATETIME,
)
GO

CREATE TABLE DOITUONGTHUHUONG(
   MADOITUONG CHAR(10)NOT NULL PRIMARY KEY,
   TENDOITUONG NVARCHAR(100),
   CMND CHAR(10),
   SDTDOITUONG CHAR(10),
   DIACHI NVARCHAR(200),
   HOTRO NVARCHAR(50),
   NGAYHOTRO DATETIME,
)
GO
CREATE TABLE QUYPHUONGTIEN(
   MAQUY CHAR(10)NOT NULL,
   TENQUY NVARCHAR(100),
   TIENDG MONEY,
   TIENHT MONEY,
   HIENVAT NVARCHAR,
   MANHT CHAR(10)NOT NULL ,
   MADOITUONG CHAR(10)NOT NULL ,
   MATQ CHAR(10)NOT NULL ,
   MANV CHAR(10)NOT NULL ,
PRIMARY KEY (MAQUY,MANHT,MADOITUONG,MATQ,MANV),
 FOREIGN KEY (MANHT) REFERENCES NHAHAOTAM(MANHT),
 FOREIGN KEY (MADOITUONG) REFERENCES DOITUONGTHUHUONG(MADOITUONG),
 FOREIGN KEY (MATQ) REFERENCES THUQUY(MATQ),
 FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV),
)
GO



